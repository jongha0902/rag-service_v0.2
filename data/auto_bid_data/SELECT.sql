  SELECT
		        CASE WHEN SUBSTR(TRADE_SEQ,1,1) = '-' THEN 
					TO_CHAR( TO_DATE(TRADE_YMD  || SUBSTR( TRADE_SEQ, 2,4),'YYYYMMDDHH24MI') -1 , 'YYYYMMDDHH24')
				ELSE  
					CASE WHEN SUBSTR(TRADE_SEQ,1,1) = '+' THEN 
						TO_CHAR(TO_DATE(TRADE_YMD  || SUBSTR( TRADE_SEQ, 2,4),'YYYYMMDDHH24MI') + 1 ,'YYYYMMDDHH24')
					ELSE 
						TO_CHAR(TO_DATE(TRADE_YMD  || SUBSTR( TRADE_SEQ, 2,4),'YYYYMMDDHH24MI') ,'YYYYMMDDHH24')
					END
				END    TRADE_YMDH, 
				GEN_CD, REVISION,   
		        BID_QT, PBID_QT, FUEL_QT, TA_QT, PTA_QT, CONST_QT,
		        CONST_TYPE, CONST_REASON, OPER_BIT,
		        AGC_FLG, GF_FLG, BSF_FLG,
		        TEMPER, A_MAX, A_MIN, GEN_CHG,
		        GF_MAX, GF_MIN, AGC_MIN, AGC_MAX,
		        ECR, EW_CONST,
		        to_char(GT_SEQ) GT_SEQ, NVL(AMB_PRESS,0) AMB_PRESS , SUB_FUEL_USE_YN,
		        WRT_ID, WRT_YMDS, NVL(A_MGMIN,0) A_MGMIN
		    FROM ETB112RT x
		    WHERE TRADE_YMD >= TO_CHAR(TO_DATE('20250705', 'YYYYMMDD') - 60, 'YYYYMMDD')
		      AND TRADE_YMD <= '20250705'
		      AND GEN_CD = '7284'
		      AND NOT (TRADE_YMD = '20250705' AND GEN_CD = '7284' AND REVISION = '0')
		    ORDER BY TRADE_YMD , GEN_CD, REVISION, TRADE_SEQ;

